private boolean validateInput(){
    movie_name= txtname.getText().trim();
   String mov_show = jcShow.getSelectedItem().toString();
   show = Integer.parseInt(mov_show);
   String cap = txtCapacity.getText().trim();
   capacity = Integer.parseInt(cap);
   screen = jcScreen.getSelectedItem().toString();
   price= Double.valueOf(txtPrice.getText());
    if(movie_name.isEmpty()||mov_show.isEmpty()||cap.isEmpty()||screen.isEmpty()||txtPrice.getText().isEmpty())
        return false;
    return true;
}

