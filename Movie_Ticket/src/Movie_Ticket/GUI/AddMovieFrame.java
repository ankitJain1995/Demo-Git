    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        boolean validate = validateInput();
         if(validate==false){
            JOptionPane.showMessageDialog(null,"UserId or Password cannot be left empty!","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        Movies movie = new Movies(movie_name, screen, capacity, show, price);
        try {
            boolean result = MovieDao.addMovie(movie);
            if(result)
            {
                JOptionPane.showMessageDialog(null,"Data Inserted Successfully");
                clearAll();
                return;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"DB Error");
            ex.printStackTrace();
            
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

