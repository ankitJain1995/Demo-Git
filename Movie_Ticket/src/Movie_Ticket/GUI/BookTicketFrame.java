    private void btnBookTicktActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookTicktActionPerformed
        // TODO add your handling code here:
        boolean isValidate = validateInput();
        if(!isValidate)
        {
            JOptionPane.showMessageDialog(this,"Please fill all the Fields..","Error!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
           SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
           Tickets tikt = new Tickets();
           tikt.setMovie_name(mov_name);
           tikt.setTicket_date(sdf.format(ticket_date));
           tikt.setUser_name(user_name);
           tikt.setContact(contact);
           tikt.setTicket(ticket);
           tikt.setScreen_no(screen);
           tikt.setPrice(price);
           
           int bookings=TicketDAO.bookedTickets(tikt.getTicket_date(),tikt.getMovie_name());
           
           System.out.println(bookings);
           System.out.println(bookings+tikt.getTicket());
           System.out.println(m.getCapacity());
           if((bookings+tikt.getTicket())<=m.getCapacity())
           {
             boolean ans = TicketDAO.bookTicket(tikt);
            if(ans)
            {
                JOptionPane.showMessageDialog(this,"Ticket Booked Successfully!!");
                clearAll();
            }  
           } 
           else{
               JOptionPane.showMessageDialog(this,"House Full !!");
               // jcMov_name.setSelectedIndex(0);
               clearAll();
           }
               
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "DB Error!!");
            e.printStackTrace();
            return;
        } catch (ParseException ex) {
           JOptionPane.showMessageDialog(this,"Parse Exception");
           ex.printStackTrace();
           return;
    }//GEN-LAST:event_btnBookTicktActionPerformed
    }
