public void loadMovieName(){
    try {
        
        ArrayList<String> list = new ArrayList<String>();
        map = MovieDao.getAllMovieName();
        if(map.isEmpty())
        {
            JOptionPane.showMessageDialog(this,"No Records Found!!");
            return;
        }
        for(Entry<String,Movies> e:map.entrySet())
        {
            list.add(e.getKey());
        }
        for(String name:list)
        {
            System.out.println(name);
            jcMov_name.addItem(name);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this,"DB Error!!");
        e.printStackTrace();
    }
}

private boolean validateInput()
{
    mov_name = jcMov_name.getSelectedItem().toString();
    user_name = txtUser_name.getText();
    screen = txtScreen.getText().trim();
   ticket_date = jDate.getDate();
   ticket = Integer.parseInt(txtTicket.getText().trim());
   price = Double.parseDouble(txtPrice.getText().trim());
    contact = txtContact.getText().trim();
    
    if(mov_name.isEmpty()||user_name.isEmpty()||screen.isEmpty()||txtTicket.getText().isEmpty()||ticket_date==null||contact.isEmpty())
        return false;
    return true;
}

